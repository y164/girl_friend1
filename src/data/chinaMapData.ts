export function normalizeProvinceName(name: string): string {
  const normalized = name
    .replace(/市$/, '')
    .replace(/省$/, '')
    .replace(/自治区$/, '')
    .replace(/壮族自治区$/, '')
    .replace(/回族自治区$/, '')
    .replace(/维吾尔自治区$/, '')
    .replace(/特别行政区$/, '')
  return normalized
}
